valor = input()
valor = float(valor)

print('NOTAS:')
print('{:d} nota(s) de R$ 100.00'.format(int(valor/100)))
print('{:d} nota(s) de R$ 50.00'.format(int(valor % 100 / 50)))
print('{:d} nota(s) de R$ 20.00'.format(int(valor % 100 % 50 / 20)))
print('{:d} nota(s) de R$ 10.00'.format(int(valor % 100 % 50 % 20 / 10)))
print('{:d} nota(s) de R$ 5.00'.format(int(valor % 100 % 50 % 20 % 10 / 5)))
print('{:d} nota(s) de R$ 2.00'.format(int(valor % 100 % 50 % 20 % 10 % 5 / 2)))
valor2 = float(valor % 100 % 50 % 20 % 10 % 5 % 2)
print('MOEDAS:')
print('{:d} moeda(s) de R$ 1.00'.format(int(valor2 / 1)))
print('{:d} moeda(s) de R$ 0.50'.format(int(valor2 % 1 / 0.5)))
print('{:d} moeda(s) de R$ 0.25'.format(int(valor2 % 1 % 0.5 / 0.25)))
print('{:d} moeda(s) de R$ 0.10'.format(int(valor2 % 1 % 0.5 % 0.25 / 0.10)))
print('{:d} moeda(s) de R$ 0.05'.format(int(valor2 % 1 % 0.5 % 0.25 % 0.10 / 0.05)))
print('{:.0f} moeda(s) de R$ 0.01'.format(float(valor2 % 1 % 0.5 % 0.25 % 0.10 % 0.05 / 0.01)))